#include <string.h>
#include <stdio.h>

void	ft_bzero(void *s, size_t n)
{
	int			i;
	char		*a;

	a = s;
	i = 0;
	while (n > 0)
	{
		a[i] = 0;
		i++;
		n--;
	}
}
